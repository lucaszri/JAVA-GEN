package br.com.generation.variaveis;

public class Variavel03 {
	
	public static void main(String[] args) {
		
		int var1 = 10;
		double var2 = 10.0;
		
		System.out.println("Valor original var1: " + var1);
		System.out.println("Valor original var2: " + var2);
		
		var1 = var1 / 4;
		var2 = var2 / 4;
		
		System.out.println("Valor var1 / 4: " + var1);
		System.out.println("Valor var2 / 4: " + var2);
	}

}
